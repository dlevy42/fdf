#include "fdf.h"

void	mod_line(char *line)
{
	int	i;

	i = 0;
	while (line[i])
	{
		if (ft_isdigit(line[i]) == 1)
			i++;
		else if (line[i] == '-')
			i++;
		else
			line[i] = ' ';
	}
}
